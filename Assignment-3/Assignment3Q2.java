//Create a program that demonstrates narrowing conversion from double to int and prints the result.

class Conversion
{
   public static void main(String Args[])
{
   double num1 = 15.5;
   int num2 = (int) num1;
 
   System.out.println("num1 :   " + num1);
   System.out.println("num2 :   " + num2);
}
}